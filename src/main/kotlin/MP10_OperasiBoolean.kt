fun main() {
    //operasi boolean yaitu operasi yang menghasilkan true dan false yang yang di dapat dari dua data
    //yaitu data true dan false
    // &&
    // ||
    // !
    val nilaia: Int = 10
    val nilaib: Int = 20

    val hasil: Boolean = nilaia == 10 && nilaib == 20

    val exampa = nilaia > 10
    val exampb = nilaib < 20

    val hasil2 = exampa || exampb

    println(hasil)
    println(hasil2)
}